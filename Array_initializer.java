package arrays.in.java;

public class Array_initializer {
    public static void main(String[] args) {
        //Array initializer
        int array[]={1,2,3,4,5};
        //Traversing an element
        for(int i=0;i<array.length;i++){
            System.out.println(array[i]);
        }
    }
}
